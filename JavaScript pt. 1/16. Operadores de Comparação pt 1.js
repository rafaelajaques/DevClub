/* 
Operadores de Comparação

== ⟶ Igual // Ele compara o valor, mas não compara o tipo de valor --> NÃO USAR
=== ⟶ Totalmente igual // Ele compara o tipo de dado

!= ⟶ Diferente // Ele compara o valor, mas não compara o tipo de valor --> NÃO USAR
!== ⟶ Totalmente diferente // Ele compara o tipo de dado
*/

const firstNumber = 23
const seccondNumber = "23"

console.log(firstNumber == seccondNumber)
console.log(firstNumber === seccondNumber)

console.log(firstNumber != seccondNumber)
console.log(firstNumber !== seccondNumber)