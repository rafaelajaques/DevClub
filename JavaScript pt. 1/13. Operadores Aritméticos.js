/* 
Operadores aritméticos

+ ⟶ adição
- ⟶ subtração
* ⟶ multiplicação
/ ⟶ divisão
% ⟶ resto
++ ⟶ incremento
-- ⟶ decremento
** ⟶ exponencial
*/

let myNumber = 10
myNumber++
myNumber++
myNumber++

console.log(myNumber)

console.log(--myNumber) // primeiro incluir o sinal de incremento / decremento e depois o número

console.log(2 ** 3)