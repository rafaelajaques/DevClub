/* 
Operadores de Comparação

> ⟶ maior
>= ⟶ maior ou igual
< ⟶ menor
<= ⟶ menor ou igual

*/

const firstNumber = 10
const seccondNumber = 20

console.log(firstNumber > seccondNumber)
console.log(firstNumber >= seccondNumber)
console.log(firstNumber < seccondNumber)
console.log(firstNumber <= seccondNumber)


